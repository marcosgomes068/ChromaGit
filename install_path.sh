# Adicionar ao PATH
add_to_path() {
    local path_line="export PATH=\"\$HOME/.local/bin:\$PATH\""
    
    print_info "${GEAR} Adicionando ~/.local/bin ao PATH..."
    
    # Verificar se jÃ¡ estÃ¡ no PATH
