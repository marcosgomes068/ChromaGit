# Detectar sistema operacional
detect_os() {
    if [[ "$OSTYPE" == "linux-gnu"* ]]; then
        OS="linux"
        SHELL_RC="$HOME/.bashrc"
        if [[ -f "$HOME/.zshrc" ]]; then
            SHELL_RC="$HOME/.zshrc"
        fi
    elif [[ "$OSTYPE" == "darwin"* ]]; then
        OS="macos"
        SHELL_RC="$HOME/.zshrc"
        if [[ -f "$HOME/.bash_profile" ]]; then
            SHELL_RC="$HOME/.bash_profile"
        fi
    else
        print_error "Sistema operacional nÃ£o suportado: $OSTYPE"
        exit 1
    fi
}

# Verificar se ChromaGit estÃ¡ instalado
check_chromagit() {
    SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
    CHROMAGIT_DIR="$SCRIPT_DIR"
    
    # Verificar se main.py existe
    if [[ ! -f "$CHROMAGIT_DIR/main.py" ]]; then
        print_error "main.py nÃ£o encontrado em: $CHROMAGIT_DIR"
        print_info "Execute este script na pasta raiz do ChromaGit"
        exit 1
    fi
    
    print_success "ChromaGit encontrado em: $CHROMAGIT_DIR"
    return 0
}

